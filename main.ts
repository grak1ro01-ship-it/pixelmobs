WA.onInit().then(() => {
    const predictions = [
        "Твой сетевой код сегодня будет работать без багов. Главное — не трогай прод.",
        "Внимательно следи за таймингами. Твой личный ролл 50/50 уже близко.",
        "Матрица перезагружается. Оглянись вокруг, возможно, мимо прошёл сбой.",
        "Твои внутренние импланты работают на полную мощность. Ошибок не обнаружено."
    ];

    WA.room.onEnterZone('girl_zone', () => {
        // Показываем в чате, что девочка начала печатать
        try {
            WA.chat.startTyping({ scope: 'local', author: 'Девочка-Андроид' });
        } catch(e) {}

        // Через 1.5 секунды «отправляем» предсказание альтернативным системным уведомлением
        setTimeout(() => {
            try {
                WA.chat.stopTyping({ scope: 'local' });
            } catch(e) {}

            const randomText = predictions[Math.floor(Math.random() * predictions.length)];
            
            // Альтернатива чату: выводим красивое всплывающее окно-уведомление прямо на экран
            WA.ui.displayNotification(randomText, {
                type: "info",
                author: "Девочка-Андроид"
            });
        }, 1500);
    });
});
